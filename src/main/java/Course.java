public class Course {
    private int swimming;
    private int run;
    private int jumping;

    public Course(int swimming, int run, int jumping){
        this.int = swimming;
        this.int = run;
        this.int = jumping;

    }
    public void setSwimming(int swimming){
        this.swimming = swimming;
    }
    public int getSwimming() {
        return swimming;
    }
    public void setRun(int run) {
        this.run = run;
    }
    public int getRun() {
        return run;
    }
    public void setJumping(int jumping) {
        this.jumping = jumping;
    }
    public int getJumping() {
        return jumping;
    }


}
